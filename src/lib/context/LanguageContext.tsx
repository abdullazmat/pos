const normalizeLanguage = (value?: string): Language => {
  const normalized = String(value || "")
    .toLowerCase()
    .split("-")[0];

  return (SUPPORTED_LANGUAGES as readonly string[]).includes(normalized)
    ? (normalized as Language)
    : "en";
};

